#Todos los datos en Python se manejan mediante referencias.
#Tipos inmutables: Aunque se manejan por referencia, no pueden ser modificados directamente.
# Modificar uno crea un nuevo objeto.
#Tipos mutables: Se manejan por referencia y pueden ser modificados directamente, 
#afectando todas las referencias al objeto.
#En Python, no hay una distinción explícita entre tipos de valor y tipos de referencia como en otros lenguajes. 
#Todos los datos son referencias, y el comportamiento depende de si el objeto es mutable o inmutable.
